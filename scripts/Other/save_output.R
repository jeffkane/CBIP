################################################################################
# This script saves the ouput from wildfire modeling. This is part of the CA 
# Biopower Impact Project.
#
# tile_number: numeric tile number.
#
# Author: Micah Wright, Humboldt State University
################################################################################

save_output <- function(dt,
                        Silvicultural_Treatment,
                        Harvest_System,
                        Harvest_Type,
                        Burn_Type,
                        tile_number,
                        year) {
        
        emissions_df <- dt[, list(x, 
                                  y,
                                  fuelbed_number,
                                  FCID2018,
                                  ID, 
                                  Silvicultural_Treatment,
                                  Harvest_Type,
                                  Harvest_System,
                                  Burn_Type,
                                  Biomass_Collection, 
                                  Year,
                                  total_char, 
                                  flaming_CH4,
                                  flaming_CO, 
                                  flaming_CO2,
                                  flaming_NH3,
                                  flaming_NOx, 
                                  flaming_PM10,
                                  flaming_PM2.5, 
                                  flaming_SO2,
                                  flaming_VOC, 
                                  smoldering_CH4, 
                                  smoldering_CO, 
                                  smoldering_CO2,
                                  smoldering_NH3,
                                  smoldering_NOx, 
                                  smoldering_PM10, 
                                  smoldering_PM2.5,
                                  smoldering_SO2, 
                                  smoldering_VOC,
                                  residual_CH4, 
                                  residual_CO, 
                                  residual_CO2,
                                  residual_NH3, 
                                  residual_NOx, 
                                  residual_PM10, 
                                  residual_PM2.5, 
                                  residual_SO2,
                                  residual_VOC, 
                                  total_CH4,
                                  total_CO, 
                                  total_CO2, 
                                  total_NH3,
                                  total_NOx,
                                  total_PM10,
                                  total_PM2.5, 
                                  total_SO2, 
                                  total_VOC)]
        
        residual_df <- dt[, list(x, 
                                 y,
                                 fuelbed_number,
                                 FCID2018, 
                                 ID, 
                                 Silvicultural_Treatment,
                                 Harvest_Type,
                                 Harvest_System,
                                 Burn_Type,
                                 Biomass_Collection, 
                                 Year,
                                 Slope,
                                 Fm10,
                                 Fm1000,
                                 Wind_corrected,
                                 duff_upper_loading,
                                 litter_loading, 
                                 one_hr_sound, 
                                 ten_hr_sound,
                                 hun_hr_sound,
                                 oneK_hr_sound,
                                 oneK_hr_rotten,
                                 tenK_hr_sound, 
                                 tenK_hr_rotten,
                                 tnkp_hr_sound,
                                 tnkp_hr_rotten,
                                 pile_field,
                                 pile_landing)]
        
        # save output
        saveRDS(emissions_df,
                file = paste0("data/Tiles/output/emissions/",
                              tile_number,
                              "/",
                              paste(Silvicultural_Treatment,
                                    Harvest_System,
                                    Harvest_Type,
                                    Burn_Type,
                                    tile_number,
                                    year, 
                                    sep = "-"),
                              ".rds"))
        
        saveRDS(residual_df,
                file = paste0("data/Tiles/output/residual_fuels/",
                              tile_number,
                              "/",
                              paste(Silvicultural_Treatment,
                                    Harvest_System,
                                    Harvest_Type,
                                    Burn_Type,
                                    tile_number,
                                    year, 
                                    sep = "-"),
                              ".rds"))
}